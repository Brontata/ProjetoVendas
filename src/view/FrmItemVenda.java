    private void cmbProdutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbProdutoActionPerformed
        //Trocando a descrição, quantidade, Preço Unitário e calculando Subtotal de acordo com a alteração em cmbProduto
        try {
            if(cmbProduto.getSelectedIndex() > -1) {
                txtDescricao.setText(pro.get(cmbProduto.getSelectedIndex()).getDescricao());
                txtQuantidade.setValue(1);
                txtPrecoUnit.setText(nfPreco.valueToString(pro.get(cmbProduto.getSelectedIndex()).getPrecoUnit()));
                txtSubtotal.setText(nfPreco.valueToString(Integer.valueOf(txtQuantidade.getText()) * pro.get(cmbProduto.getSelectedIndex()).getPrecoUnit()));
            }
        } catch (ArrayIndexOutOfBoundsException e) {
            JOptionPane.showMessageDialog(null, "Ocorreu um erro:\n"
                    + "O código digitado não corresponde a um produto cadastrado!\n"
                    + "Valor: " + e.getMessage(), "Erro:", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Ocorreu um erro:\n" + e.getMessage(), "Erro:", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }//GEN-LAST:event_cmbProdutoActionPerformed

