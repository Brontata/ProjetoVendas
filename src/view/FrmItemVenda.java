    private FrmItemVenda(DaoItemVenda item) throws SQLException, ClassNotFoundException {
        
        initComponents();
        //Centralizando o diálogo
        Dimension screen = Toolkit.getDefaultToolkit().getScreenSize();
        int x = (screen.width - this.getWidth()) / 2;
        int y = (screen.height - this.getHeight()) / 2;
        //Colocando os valores em FrmItemVenda
        setBounds(x, y, this.getWidth(), this.getHeight());
        //Passando o Numero da venda para txtNumVenda
        System.out.println(item.getNumVenda());
        txtNumVenda.setText(String.valueOf(item.getNumVenda()));
        //Travando a aplicação ao Diálogo
        setModal(true);
        setModalityType(Dialog.ModalityType.APPLICATION_MODAL);
        try {
            //Formato para os valores monetários
            DecimalFormat df = new DecimalFormat("R$ #,###,##0.00;-R$ #,###,##0.00");
            nfPreco = new NumberFormatter(df);
            nfPreco.setValueClass(Double.class);
            //Formato para txtQuantidade
            DecimalFormat dfn = new DecimalFormat("###0");
            nfNumero = new NumberFormatter(dfn);
            nfNumero.setValueClass(Integer.class);
            DefaultFormatterFactory dffQtd = new DefaultFormatterFactory(nfNumero);
            txtQuantidade.setFormatterFactory(dffQtd);
            //Preenchendo o ArrayList 'pro' e o cmbProduto com os dados
            DaoProduto produto = new DaoProduto();
            pro = new ArrayList<DaoProduto>();
            pro.addAll(produto.pesquisar(produto));
            cmbProduto.removeAllItems();
            for (DaoProduto daoPro : pro) {
               cmbProduto.addItem(daoPro.getCodPro());
            }
            //Passando o produto do item para cmbProduto
            if (item.getCodPro() != 0) {
                cmbProduto.setSelectedItem(item.getCodPro());
                //Passando a quantidade do produto do item para txtQuantidade
                txtQuantidade.setValue(item.getQuantidade());
            } else {
                cmbProduto.setSelectedItem(0);
            }
            //Tornando esse JDialog visível
            setVisible(true);
        } catch (SQLException e) {
            throw new SQLException("Ocorreu um erro no Formulário de vendas: \n" + e.getMessage(), "Erro");
        } catch (ClassNotFoundException e) {
            throw new ClassNotFoundException("Ocorreu um erro no Formulário de vendas: \n" + e.getMessage());
        }
    }
    
    public static DaoItemVenda showItemVenda(DaoItemVenda item) throws SQLException, ClassNotFoundException {
        FrmItemVenda frmItemVenda = new FrmItemVenda(item);
        return itemRetorno;
        
    }

